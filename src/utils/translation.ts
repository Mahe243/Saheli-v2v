export function useTranslation() {
  const [lang, setLang] = useState(() => {
    return localStorage.getItem("saheli_preferred_language") || "English";
  });

  useEffect(() => {
    const handleUpdate = () => {
      const current = localStorage.getItem("saheli_preferred_language") || "English";
      setLang(current);
    };

    // Listen to changes via custom event or orchestrator updates
    const unsub = orchestrator.subscribeAll(handleUpdate);
    window.addEventListener("storage", handleUpdate);

    return () => {
      unsub();
      window.removeEventListener("storage", handleUpdate);
    };
  }, []);

  const t = (key: string): string => {
    const dict = translationDictionary[lang] || translationDictionary["English"];
    return dict[key] || translationDictionary["English"][key] || key;
  };

  return { t, currentLanguage: lang };
}
